/**
 * 첫 번째 정수를 두 번째 정수로 나눈 몫을 출력하는 함수
 *
 * @param {number} num1 첫 번째 정수
 * @param {number} num2 두 번째 정수
 * @returns {number} 첫 번째 정수를 두 번째 정수로 나눈 몫
 */
function solution(num1, num2) {
  let answer = parseInt(num1 / num2);

  return answer;
}
